normalize_double() {
  local value="$1"
  if [[ "$value" == *.* ]] || [[ "$value" == *e* ]] || [[ "$value" == *E* ]]; then
    printf '%s' "$value"
  else
    printf '%s.0' "$value"
  fi
}

